    private void EDIT_BUTTONActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EDIT_BUTTONActionPerformed

        String employeeID = jTextFieldID1_EmployeeID.getText();
        String name = jTextFieldID_Name.getText();
        String faculty = jComboBox1_faculty.getSelectedItem().toString();
        String department = jComboBox2_department.getSelectedItem().toString();
        String center = jComboBox3_center.getSelectedItem().toString();
        String building = jComboBox4_building.getSelectedItem().toString();
        String level = jComboBox5_level.getSelectedItem().toString();
        String rank = jComboBox5_level.getSelectedItem().toString()+"."+jTextFieldID1_EmployeeID.getText();

        if(name.trim().equals("") || faculty.trim().equals("Select Faculty") || department.trim().equals("Select Department") || center.trim().equals("Select Center") 
                || building.trim().equals("Select Building") || level.trim().equals("Select Level") || rank.trim().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Some fields are empty", "Empty Field", JOptionPane.WARNING_MESSAGE);
        }
        
        else if(!(Pattern.matches("^[0-9]{6}+$",jTextFieldID1_EmployeeID.getText()))){
            JOptionPane.showMessageDialog(rootPane, "Please enter valid Employee ID", "Fill Details Error", JOptionPane.ERROR_MESSAGE);
        }
       
        else{
            try{
                employeeID = String.valueOf(jTextFieldID1_EmployeeID.getText());

                if(lecturer.editLecturer(employeeID,name,faculty,department,center,building,level,rank)){
                    JOptionPane.showMessageDialog(rootPane, "Lecturer Data Updated Successfully", "Edit Lecturer", JOptionPane.INFORMATION_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Lecturer Data Not Updated", "Edit Lecturer Error", JOptionPane.ERROR_MESSAGE);
                }
            }
            catch(NumberFormatException ex){
                JOptionPane.showMessageDialog(rootPane, ex.getMessage() + " - Enter The Lecturer Id(number)", "Lecturer ID Error", JOptionPane.ERROR_MESSAGE);
            }
         }  
        
        //clear the jtable first
        Lecturers_table.setModel(new DefaultTableModel(null, new Object[]{"name","employee_id","faculty","department","center","building","level","rank"}));
        //populate the jtable
        lecturer.fillLecturersJTable1(Lecturers_table);
        
        Lecturers_table1.setModel(new DefaultTableModel(null, new Object[]{"name","employee_id","faculty","department","center","building","level","rank"}));
        //populate the jtable
        lecturer.fillLecturersJTable1(Lecturers_table1);
        
        jTextFieldID_Name.setText(" ");
        jTextFieldID1_EmployeeID.setText(" ");
        jComboBox1_faculty.setSelectedItem("Select Faculty");
        jComboBox2_department.setSelectedItem("Select Department");
        jComboBox3_center.setSelectedItem("Select Center");
        jComboBox4_building.setSelectedItem("Select Building");
        jComboBox5_level.setSelectedItem("Select Level");
        jTextFieldID7_Rank.setText(" ");
        
    }//GEN-LAST:event_EDIT_BUTTONActionPerformed

