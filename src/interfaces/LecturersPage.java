    private void SEARCH_BUTTONActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SEARCH_BUTTONActionPerformed
        
        String name = jTextField1_Search.getText();
        String sql = "SELECT `name`, `employee_id`, `faculty`, `department`, `center`, `building`, `level`, `rank` FROM `lecturers` WHERE `name` LIKE '%" + name + "%'";

        try {
            PreparedStatement pst;
            ResultSet rs;
            pst = myConnection.createConnection().prepareStatement(sql);
            rs = pst.executeQuery();
            
        Lecturers_table.setModel(DbUtils.resultSetToTableModel(rs));   
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage() , "Search Details", JOptionPane.ERROR_MESSAGE);
        }
        
        jTextField1_Search.setText(" ");
    }//GEN-LAST:event_SEARCH_BUTTONActionPerformed

